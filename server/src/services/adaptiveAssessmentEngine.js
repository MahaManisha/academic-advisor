const UserAnalytics = require('../models/UserAnalytics');
const { generateLLMResponse } = require('../utils/llmHelper');

/**
 * Adaptive Assessment Engine Service
 *
 * Generates adaptive questions using an LLM. It relies on the previous
 * responses from the database and the calculated analytics state to
 * dynamically tailor the interview difficulty and topic.
 */

